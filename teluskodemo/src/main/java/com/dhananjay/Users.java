package com.dhananjay;

public class Users {
	
	private String uname;
	private int uage;
	private int uid;
	private String uemail;
	
	
	public Users(){
		
	}
		
		
	
	/**
	 * @return the uname
	 */
	public String getUname() {
		return uname;
	}
	/**
	 * @param uname the uname to set
	 */
	public void setUname(String uname) {
		this.uname = uname;
	}
	/**
	 * @return the uage
	 */
	public int getUage() {
		return uage;
	}
	/**
	 * @param uage the uage to set
	 */
	public void setUage(int uage) {
		this.uage = uage;
	}
	/**
	 * @return the uid
	 */
	public int getUid() {
		return uid;
	}
	/**
	 * @param uid the uid to set
	 */
	public void setUid(int uid) {
		this.uid = uid;
	}
	/**
	 * @return the uemail
	 */
	public String getUemail() {
		return uemail;
	}
	/**
	 * @param uemail the uemail to set
	 */
	public void setUemail(String uemail) {
		this.uemail = uemail;
	}
	
	
	
	
	
	
	
	
	

}
